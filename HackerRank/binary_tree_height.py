def traverse(root, count) -> int:
    if(root == None):
        return count
    count += 1
    return max(traverse(root.left, count), traverse(root.right, count))


def height(root) -> int:
    return traverse(root, -1)


tree = BinarySearchTree()
t = int(input())

arr = list(map(int, input().split()))

for i in range(t):
    tree.create(arr[i])

print(height(tree.root))
